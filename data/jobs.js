// Function to get jobs with pagination and filtering
export async function getJobs({
page = 1,
pageSize = 6,
department = "all",
}) {
// Filter jobs by department if specified
const filteredJobs = department === "all" ? jobsData : jobsData.filter((job) => job.department === department)

// Calculate pagination
const totalJobs = filteredJobs.length
const totalPages = Math.ceil(totalJobs / pageSize)
const startIndex = (page - 1) * pageSize
const endIndex = startIndex + pageSize
const paginatedJobs = filteredJobs.slice(startIndex, endIndex)

// Get unique departments for filtering
const departments = Array.from(new Set(jobsData.map((job) => job.department)))

// Simulate API delay
await new Promise((resolve) => setTimeout(resolve, 500))

return {
    jobs: paginatedJobs,
    totalJobs,
    totalPages,
    departments,
}
}

// Function to get a single job by ID
export async function getJobById(id) {
// Simulate API delay
await new Promise((resolve) => setTimeout(resolve, 300))

const job = jobsData.find((job) => job.id === id)
return job || null
}

// Function to get related jobs
export async function getRelatedJobs(id, department, limit = 3) {
// Simulate API delay
await new Promise((resolve) => setTimeout(resolve, 300))

// Get jobs from the same department, excluding the current job
const relatedJobs = jobsData.filter((job) => job.id !== id && job.department === department).slice(0, limit)

// If not enough related jobs in the same department, add jobs from other departments
if (relatedJobs.length < limit) {
    const otherJobs = jobsData
    .filter((job) => job.id !== id && job.department !== department)
    .slice(0, limit - relatedJobs.length)

    relatedJobs.push(...otherJobs)
}

return relatedJobs
}
  